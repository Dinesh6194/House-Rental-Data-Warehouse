/* Create table for Clinic,Doctor,Patient,Service and Assignment*/
CREATE TABLE CLINIC(
hospital_id VARCHAR(20) PRIMARY KEY,
hospital_name VARCHAR(50) NOT NULL,
hospital_address VARCHAR(100) NOT NULL,
suburb VARCHAR(20) NOT NULL,
postcode NUMBER(6) NOT NULL
);

CREATE TABLE DOCTOR(
staff_id VARCHAR(20) PRIMARY KEY,
staff_name VARCHAR(50) NOT NULL,
staff_ph VARCHAR(10) NOT NULL
);

CREATE TABLE PATIENT(
patient_id VARCHAR(20) PRIMARY KEY,
patient_name VARCHAR(50) NOT NULL,
patient_age NUMBER(3) NOT NULL,
patient_ph_no VARCHAR(10) NOT NULL,
patient_address VARCHAR(100) NOT NULL,
patient_nationality VARCHAR(20) NOT NULL,
patient_emergency_contact VARCHAR(10) NOT NULL
);

CREATE TABLE SERVICE(
service_id VARCHAR(20) PRIMARY KEY,
staff_id VARCHAR(20) REFERENCES DOCTOR(staff_id),
hospital_id VARCHAR(20) REFERENCES CLINIC(hospital_id),
service_name VARCHAR(50) NOT NULL,
service_cost NUMBER(10) NOT NULL
);

CREATE TABLE ASSIGNMENT(
assignment_id VARCHAR(20) PRIMARY KEY,
patient_id VARCHAR(20) REFERENCES PATIENT(patient_id),
patient_service_start_date DATE NOT NULL,
patient_service_end_date DATE NOT NULL,
service_id VARCHAR(20) REFERENCES SERVICE(service_id)
);

